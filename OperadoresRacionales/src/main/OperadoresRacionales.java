package main;

public class OperadoresRacionales {

	public static void main(String[] args) {
		// Operadores Racionales
		
		int variable1 = 300, variable2 = 200;
		boolean resultado;
		
		
		resultado = variable1 > variable2;
		System.out.println(resultado);
		
		resultado = variable1 < variable2;
		System.out.println(resultado);
		
		resultado = variable1 >= variable2;
		System.out.println(resultado);
		
		resultado = variable1 <= variable2;
		System.out.println(resultado);
		
		resultado = variable1 == variable2;
		System.out.println(resultado);
		
		resultado = variable1 != variable2;
		System.out.println(resultado);

	}

}
